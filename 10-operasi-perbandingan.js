// >    Lebih Dari
// <    Kurang Dari  
// >=   Lebih Dari Sama Dengan
// <=   Kurang Dari Sama Dengan
// ==   Sama Dengan
// ===  Sama Dengan dan Sama Tipe
// !=   Tidak Sama Dengan
// !==  Tidak Sama Dengan atau Tidak Sama Tipe

let result = 5 == 5
console.log(result)

result = 5 == "5";
console.log(result)

result = 5 === "5";
console.log(result)

result = 5 < 5;
// result = 2 < 3;
console.log(result)

result = 5 <= 5;
// result = "2" <= 5;
console.log(result)

result = 10 > 5;
console.log(result)

result = 10 != "10";
console.log(result)

result = 10 !== "10";
console.log(result)